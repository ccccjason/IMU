//初始化IO 串口2
//pclk1:PCLK1时钟频率(Mhz)
//bound:波特率
void USART2_Init(u32 bound)
{

    NVIC_InitTypeDef NVIC_InitStructure;
    GPIO_InitTypeDef GPIO_InitStructure;
    USART_InitTypeDef USART_InitStructure;

    RCC_AHBPeriphClockCmd(RCC_AHBPeriph_GPIOA, ENABLE); // GPIOA时钟
    RCC_APB1PeriphClockCmd(RCC_APB1Periph_USART2, ENABLE);

    GPIO_PinAFConfig(GPIOA, GPIO_PinSource2, GPIO_AF_1);
    GPIO_PinAFConfig(GPIOA, GPIO_PinSource3, GPIO_AF_1);

    USART_DeInit(USART2);  //复位串口2
    //USART2_TX   PA.2  USART2_RX    PA.3
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_2 | GPIO_Pin_3;
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_Level_2;
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF;    //复用推挽输出
    GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;
    GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;
    GPIO_Init(GPIOA, &GPIO_InitStructure); //初始化


    USART_InitStructure.USART_BaudRate = bound;//一般设置为9600;
    USART_InitStructure.USART_WordLength = USART_WordLength_8b;//字长为8位数据格式
    USART_InitStructure.USART_StopBits = USART_StopBits_1;//一个停止位
    USART_InitStructure.USART_Parity = USART_Parity_No;//无奇偶校验位
    USART_InitStructure.USART_HardwareFlowControl =
        USART_HardwareFlowControl_None;//无硬件数据流控制
    USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx; //收发模式

    USART_Init(USART2, &USART_InitStructure); //初始化串口  2


    UART_DMA_Config(DMA1_Channel4, (u32)&USART2->TDR,
                    (u32)USART2_TX_BUF); //DMA1通道7,外设为串口2,存储器为USART2_TX_BUF
    USART_DMACmd(USART2, USART_DMAReq_Tx, ENABLE);  //使能串口2的DMA发送
    USART_Cmd(USART2, ENABLE);                    //使能串口


#ifdef USART2_RX_EN         //如果使能了接收
    //使能接收中断
    USART_ITConfig(USART2, USART_IT_RXNE, ENABLE);//开启中断

    NVIC_InitStructure.NVIC_IRQChannel = USART2_IRQn;
    NVIC_InitStructure.NVIC_IRQChannelPriority = 2;
    NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;         //IRQ通道使能
    NVIC_Init(&NVIC_InitStructure); //根据指定的参数初始化VIC寄存器
    TIM3_Init(99, 4799);    //10ms中断
    USART2_RX_STA = 0;      //清零
    TIM3_Set(0);            //关闭定时器4
#endif

}
//非DMA下，测试串口配置
void UART2_send_byte(uint8_t
                     byte)                                //发送1字节数据
{
    while (!((USART2->ISR) & (1 << 7)));

    USART2->TDR = byte;
}
void UART2_send_string(char* Data)
{
    while (*Data) {
        UART2_send_byte(*Data++);
    }
}
//串口2,printf 函数
//确保一次发送数据不超过USART2_MAX_SEND_LEN字节
void USART2_printf(char* fmt, ...)
{
    va_list ap;
    va_start(ap, fmt);
    vsprintf((char*)USART2_TX_BUF, fmt, ap);
    //UART2_send_string((char*)USART2_TX_BUF);
    va_end(ap);

    while (DMA_GetCurrDataCounter(DMA1_Channel4) == 0); //等待通道7传输完成

    UART_DMA_Enable(DMA1_Channel4,
                    strlen((const char*)USART2_TX_BUF)); //通过dma发送出去
}

//设置TIM4的开关
//sta:0，关闭;1,开启;
void TIM3_Set(u8 sta)
{
    if (sta) {
        TIM_SetCounter(TIM3, 0); //计数器清空
        TIM_Cmd(TIM3, ENABLE);  //使能TIMx
    } else {
        TIM_Cmd(TIM3, DISABLE);    //关闭定时器4
    }
}

void TIM3_Init(u16 arr, u16 psc) //1000,48,48000000/48/1000=1000HZ=1ms中断
{
    NVIC_InitTypeDef NVIC_InitStructure;
    TIM_TimeBaseInitTypeDef  TIM_TimeBaseStructure;

    RCC_APB1PeriphClockCmd(RCC_APB1Periph_TIM3,
                           ENABLE); //时钟使能//TIM4时钟使能

    //定时器TIM3初始化
    TIM_TimeBaseStructure.TIM_Period =
        arr; //设置在下一个更新事件装入活动的自动重装载寄存器周期的值
    TIM_TimeBaseStructure.TIM_Prescaler = psc -
                                          1; //设置用来作为TIMx时钟频率除数的预分频值
    TIM_TimeBaseStructure.TIM_ClockDivision =
        TIM_CKD_DIV1; //设置时钟分割:TDTS = Tck_tim
    TIM_TimeBaseStructure.TIM_CounterMode = TIM_CounterMode_Up;  //TIM向上计数模式
    TIM_TimeBaseInit(TIM3,
                     &TIM_TimeBaseStructure); //根据指定的参数初始化TIMx的时间基数单位

    TIM_ITConfig(TIM3, TIM_IT_Update, ENABLE); //使能指定的TIM4中断,允许更新中断


    /*  TIM3 中断嵌套设计*/
    NVIC_InitStructure.NVIC_IRQChannel = TIM3_IRQn;
    NVIC_InitStructure.NVIC_IRQChannelPriority = 1;
    NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;
    NVIC_Init(&NVIC_InitStructure);
}
